public class Page {
    String addr;
    int bit;

    public Page(String a, int b) {
        addr = a;
        bit = b;
    }

    public String getAddr() {
        return addr;
    }

    public void setAddr(String addr) {
        this.addr = addr;
    }

    public int getBit() {
        return bit;
    }

    public void setBit(int bit) {
        this.bit = bit;
    }


}
